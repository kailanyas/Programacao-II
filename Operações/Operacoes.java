public class Operacoes{
    
    public static void main(String[] args){
        float resul1;
        float resul2;
        float resul3;
        
        resul1 = (-5 + 7 * 8);
        resul2 = (72 + 9 % 9);
        resul3 = (5 + ((15/3) * 3) - 4 % 3);
    
        System.out.printf("I: %.2f\nII: %.2f\nIII: %.2f\n", resul1, resul2, resul3);
        
    }

}
